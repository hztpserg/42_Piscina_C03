int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	if ((s1[i] == '\0' && s2[i] == '\0') || n == 0 || i == n)
		return (0);
	else if (s1[i] == '\0')
		return (s2[i] * -1);
	else
		return (s1[i]);
}
